public class Mahasiswa {
	private String nama;
	private String nim;
	
	public Mahasiswa(String nama, String nim) {
		this.nama = nama;
		this.nim = nim;
	}
	
	//setter
	public String getNama() {
		return nama;
	}
	
	public String getNim() {
		return nim;
	}
}